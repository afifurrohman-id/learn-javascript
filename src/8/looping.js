// * Looping

// ! For
for (let i = 0; i <= 5; i++) {
  console.log(i)
}

// result
// 0,1,2,3,4,5

// ! While
let a = 10
while (a <= 15) {
  console.log(a)
  a++
}

// result
// 10,11,12,13,14,15

// ! Do.. While
let value = 3
do {
  console.log('Hello')
  value++
} while (value <= 8)

// result
// Hello, Hello, Hello, Hello, Hello, Hello
