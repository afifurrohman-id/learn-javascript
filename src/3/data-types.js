// * Data Types (Primitive)

// ! String
const name = 'afif'
console.log(typeof(name))

// result 
// string

// ! Number
const a = 1
console.log(typeof(a))

// result 
// number

// ! Boolean
const isMale = true
console.log(typeof(isMale))

// result 
// boolean

// ! Object
const myBio = {
    name: 'Afif',
    id: 1,
    isMale: true
}
console.log(typeof(myBio))

// result 
// object