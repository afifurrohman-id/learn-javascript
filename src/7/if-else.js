// * Control Flow (If Else)

const a = 5

// ! If
if (a <= 6) {
    console.info('A is less than 6')
} 

// result
// A is less than 6

// ! Else If
const id = 1

if(id === 6) {
    console.info('You Winner')
} else if(id === 1) console.info('You Lose')
// result
// You Lose

// ! Else
if (a <= 3) {
    console.info('A is less than 3')
} else console.log('A is more than 3') // ? 

// result
// A is more than 3
