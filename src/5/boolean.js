// * Boolean

const a = true
const b = false

// ! And  
console.info(a && b)

// result
// ? false (because just only one is true. To return true two value must be true)

// ! Or 
console.info(a || b)

// result
// true

// ! Greater than equal
console.info(1 >= 2)

// result
// false

// ! Greater than equal
console.info(5 <= 10)

// result
// true

// ! Strict Operator
console.info(10 === '10')

// result
// false

// ! Not Operator
console.info( 8 !== 8.1)

// result
// true

