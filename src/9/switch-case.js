// * Switch Case

const lang = 'JavaScript'

switch (lang) {
  case 'java':
    console.info('OOP')
    break
  case 'c':
    console.info('Low level language')
    break
  case 'python': 
  case 'perl':
    console.info('Indentation Language')
    break
  default:
    console.info('JavaScript is a Prototype Language')
    break
}

// result
// JavaScript is a Prototype Language

